        /// Manual switch to minimal capacity.
        /// </summary>
        MinCapacity,
        /// <summary>
        /// Manual switch to maximum capacity.
        /// </summary>
        MaxCapacity,
        /// <summary>
        /// Manual switch to initial capacity.
        /// </summary>
        InitCapacity
    }
}
